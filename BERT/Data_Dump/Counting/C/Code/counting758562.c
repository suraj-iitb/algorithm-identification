#include<stdio.h>
#define ABSIZE 2000000
#define CSIZE 10000

int A[ABSIZE], B[ABSIZE], C[CSIZE];

void Counting_Sort(int n)
{
    int i, j;
    for(i = 0; i < CSIZE; i++){
        C[i] = 0;
    }
    for(j = 0; j < n; j++){
        C[A[j]] = C[A[j]]+1;
    }
    /* C[i]??????i???????????属????即???????????????????*/
    for(i = 1; i < CSIZE; i++){
        C[i] = C[i] + C[i-1];
    }
    /* C[i]??????i??\????????属???????????属????即???????????????????*/
    for(j = n-1; 0 <= j; j--){
       B[C[A[j]]-1] = A[j];
       C[A[j]] = C[A[j]]-1;
    }
}

int main()
{
  int n, i;

  scanf("%d", &n);
  for(i = 0; i < n; i++){
    scanf("%d", &A[i]);
  }

  Counting_Sort(n);

  for(i = 0; i < n-1; i++){
    printf("%d ", B[i]);
  }
  printf("%d\n", B[i]);

  return(0);
}
