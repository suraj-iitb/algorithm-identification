int selectionSort(int array[], int n)
{
	int i, j, min_num, min_idx, swap_cnt=0, temp;
	for (i=0; i<n-1; i++) {
		min_num = array[i];
		min_idx = i;
		for (j=i; j<n; j++) {
			if (array[j] < min_num) {
				min_num = array[j];
				min_idx = j;
			}
		}
		if (min_idx != i) {
			swap_cnt++;
			temp = array[i];
			array[i] = min_num;
			array[min_idx] = temp;
		}
	}
	return swap_cnt;
}
