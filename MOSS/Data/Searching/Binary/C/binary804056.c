int binarySearchTF(int key, int data[], int n){
	
	int min = 0,  max = n-1, pos = (min+max)/2;
	
	while(min <= max){
		
		if(key < data[pos]){
			
			max = pos-1;
			pos = (min+max)/2;
			
		}
		else if(key > data[pos]){
			
			min = pos+1;
			pos = (min+max)/2;
			
		}
		else{
			
			return 1;
			
		}
		
	}
	
	return 0;
	
}
