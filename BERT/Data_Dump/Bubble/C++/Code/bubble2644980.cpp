// ????????????,?????????????????¢????¬???°
int bubbleSort(int A[], int N){
	int sw = 0; // ?????¢?¬???°
	bool flag = true; // ???????????¨?????\?????¢???????´?
	for(int i = 0; flag; i++){
		flag = false;
		for(int j = N-1; j >= i+1; j--){ // ????????\.????¬??????¢?????????????????¨???????¢??????????????´?
			if(A[j-1] > A[j]){ // ???????????¢???????´??????????????´???§,?°±?????¢
				swap(A[j-1], A[j]);
				flag = true;
				sw++;
			} 
		}
	}
	return sw;
}

int main(){
	//freopen("in.txt", "r", stdin);
	int N,A[100],sw;
	while(cin >> N) {
	    for(int i = 0; i < N; i++) cin >> A[i];
	    sw = bubbleSort(A, N);
	    for(int i = 0; i < N; i++){
	    	if(i) cout << " ";
	    	cout << A[i];
	    }
	    cout << endl;
	    cout << sw << endl;
	}
}
