#include <stdio.h>
#include <string.h>

int N;
int A[100];

////////////////////////////////////////////////////////////////////////////////
//
/////////////////////////////////////////////////////////////////////////////////
void outline(void)
{
	int i;
	for ( i = 0 ; i < N ; i++ ) {
		if ( i > 0 ) {
			printf(" ");
		}
		printf("%d", A[i]);
	}
	printf("\n");
}
////////////////////////////////////////////////////////////////////////////////
//
/////////////////////////////////////////////////////////////////////////////////
int main(void){
	int i;
	int j;
	int v;

	scanf("%d", &N);

	for ( i = 0 ; i < N ; i++ ) {
		scanf("%d", &A[i]);
	}

	outline();

	for ( i = 1 ; i < N ; i++ ) {

		v = A[i];
		j = i - 1;

		while (j >= 0 && A[j] > v) {
			A[j+1] = A[j];
			j--;
			A[j+1] = v;
		}

		outline();
	}

	return 0;
}
