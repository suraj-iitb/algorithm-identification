void printArray(unsigned int Array[], unsigned int length) {
  std::ostringstream oss;
  for (size_t i = 0; i < length - 1; i++) {
    oss << Array[i] << " ";
  }
  oss << Array[length - 1];
  std::cout << oss.str() << std::endl;
}

