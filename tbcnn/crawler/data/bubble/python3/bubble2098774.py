    swap_count = 0
    flag = True
    i = 0
    while flag is True:
        flag = False
        for j in range(n-1, i, -1):
            if data[j] < data[j-1]:
                temp = data[j]
                data[j] = data[j-1]
                data[j-1] = temp
                swap_count += 1
                flag = True
        i == 1
    return swap_count, data


