#include<stdio.h>
#include<stdlib.h>

int linearSearch(int a[],int N,int key){
    int i;
    a[N] = key;
    for(i=0;i<N+1;i++){
        if(a[i]==key)break;
    }
    return i == N ? -1:i;
}

