int alds1_2(void)
{
    int i,j,k,l,n,S; int cnt, *ary;

    scanf("%d", &n);
    ary=malloc(n*sizeof(int));
    for (i=0; i<n; i++)
        scanf("%d", &ary[i]);
    for (k=cnt=0, l=n; k<n-1; k++, l--)
    for (i=0,j=1; j<l; i++, j++){
        if (ary[j]<ary[i]){
            S=ary[i];
            ary[i]=ary[j];
            ary[j]=S;
            cnt++;
        }
	}
    for (i=0; i<n; i++){
        if (i)
            printf(" ");
        printf("%d", ary[i]);
    }
    printf("\n%d\n", cnt);
}
//----------------------------------------------------------------------------
//----------------------------------------------------------------------------
int main(void)
{
    alds1_2();
    return 0;
}
