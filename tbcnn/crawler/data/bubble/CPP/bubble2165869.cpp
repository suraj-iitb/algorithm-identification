int main() {

	int n;
	scanf("%d", &n);

	int A[n];
	for (int i = 0; i < n; ++i) {
		scanf("%d", A + i);
	}

//bubbleSort
	int cnt = 0;
	for (int i = 0; i < n; ++i) {
		for (int j = n - 1; j > i; --j) {
			if (A[j] < A[j - 1]) {
				swap(A[j - 1], A[j]);
				++cnt;
			}
		}
	}

	print(A, n);
	cout << cnt << endl;

	return 0;
}
