#include <stdio.h>

/*---x??¨y???????????\????????????---*/
void swap(int *x, int *y)
{
	int temp;
	temp = *x;
	*x = *y;
	*y = temp;
}

int main(void)
{
	int A[100], N, i, j, n = 0;

	scanf("%d", &N);		//????´?????????°N???????????????
	for (i = 0; i < N; i++)
		scanf("%d", &A[i]);		//????´????????????????

	for (i = 0; i < N; i++) {		//bubble sort
		for (j = N - 1; j > i; j--)
			if (A[j] < A[j - 1]) {
				swap(&A[j], &A[j - 1]);
				n++;
			}
	}

	for (i = 0; i < N; i++)		//???????????????????´??????????
		printf("%d%c", A[i], i == N - 1 ? '\n' : ' ');

	printf("%d\n", n);		//??\?????????????????°n?????????

	return 0;
}
