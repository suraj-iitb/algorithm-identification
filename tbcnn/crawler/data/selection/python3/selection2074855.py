def selection_sort(target_list):
    list_length = len(target_list)
    change_count = 0
    for i in range(list_length):
        min_index = i
        for j in range(i, list_length):
            if target_list[j] < target_list[min_index]:
                min_index = j
        else:
            if not i == min_index:
                tmp = target_list[i]
                target_list[i] = target_list[min_index]
                target_list[min_index] = tmp
                change_count += 1
                
    return change_count
def main():
    
    n_list = int(input())
    target_list = [int(n) for n in input().split()]
    cc = selection_sort(target_list)
    print(*target_list)
    print(cc)
    
if __name__ == "__main__":
    main()
