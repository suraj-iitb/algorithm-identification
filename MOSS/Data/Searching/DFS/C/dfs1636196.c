void dfs(int n){
    
    int i;
    
    if (found[n-1]) {
        return;
    }else{
        found[n-1] = 1;
        d[n-1] = tim++;
    }
    
    for (i=0; i < NL_num[n-1]; i++) {
        if ( !found[ NL[n-1][i] -1 ] ) {
            dfs(NL[n-1][i]);
        }
    }
    f[n-1]= tim++;
    
    return;
    
}

