void insertionSort(int array[], int n)
{
	int i, j, v;
	printArray(array, n);
	for (i=1; i<n; i++) {
		v = array[i];
		j = i-1;
		while (j>=0 && array[j]>v) {
			array[j+1] = array[j];
			j--;
		}
		array[j+1] = v;
		printArray(array, n);
	}
}


