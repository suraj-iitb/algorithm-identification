void insertionSort(int A[], int N)
{
	for (int i = 0; i < N; i++)
	{
		v = A[i];
		int j = i - 1;
		while (j >= 0 && A[j]>v)
		{
			A[j + 1] = A[j];
			j--;
		}
		A[j + 1] = v;
		print(A, N);
	}
}
void print(int A[], int N)
{
	for (int i = 0; i < N; i++)
	{
		if (i >0)
		{
			cout << " ";
		}
		cout << A[i];
		
	}
	cout << endl;
}
